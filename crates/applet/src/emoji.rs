/// Get all emojis matching a search query (case-insensitive category name match,
/// or direct emoji match).
pub fn search(query: &str) -> Vec<&'static str> {
    let q = query.to_lowercase();
    let mut results = Vec::new();

    for cat in CATEGORIES {
        if cat.name.to_lowercase().contains(&q) {
            results.extend_from_slice(cat.emojis);
        } else {
            for emoji in cat.emojis {
                if emoji.contains(query) {
                    results.push(emoji);
                }
            }
        }
    }

    results
}
